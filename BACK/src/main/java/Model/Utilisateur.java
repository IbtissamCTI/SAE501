package Model;

import Enums.Role;
import jakarta.persistence.*;
import java.time.LocalDateTime;

@Entity // ON METS EN PLACE LA CLASSE UTILISTATEUR
@Table(name = "utilisateurs") // <---ET DONC SA TABLE EN SQL
public class Utilisateur {

    @Id //LES PARAMETRES DE LA TABLE UTILISATEUR DCP
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column(nullable = false)
    private String nom;

    @Column(nullable = false)
    private String prenom;

    @Column(nullable = false, unique = true)
    private String email;

    @Column(nullable = false)
    private String motDePasse;

    @Enumerated(EnumType.STRING)
    @Column(nullable = false)
    private Role role;

    private LocalDateTime dateInscription = LocalDateTime.now();


    //CONSTRUCTEUR DE LACLASSE UTILISATEUR
    public Utilisateur() {}

    public Utilisateur(String nom, String prenom, String email, String telephone,
                       String adressePostale, String pseudo, String motDePasse, Role role) {
        this.nom = nom;
        this.prenom = prenom;
        this.email = email;
        this.motDePasse = motDePasse;
        this.role = role;
    }

    // Getters et Setters
    public Long getId() { return id; }
    public void setId(Long id) { this.id = id; }

    public String getNom() { return nom; }
    public void setNom(String nom) { this.nom = nom; }

    public String getPrenom() { return prenom; }
    public void setPrenom(String prenom) { this.prenom = prenom; }

    public String getEmail() { return email; }
    public void setEmail(String email) { this.email = email; }


    public String getMotDePasse() { return motDePasse; }
    public void setMotDePasse(String motDePasse) { this.motDePasse = motDePasse; }

    public Role getRole() { return role; }
    public void setRole(Role role) { this.role = role; }

    public LocalDateTime getDateInscription() { return dateInscription; }
    public void setDateInscription(LocalDateTime dateInscription) { this.dateInscription = dateInscription; }
}